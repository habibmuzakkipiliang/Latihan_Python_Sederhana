# List Python
buat = ["Apel", "Semangka", "Melon"]
print (buat)
print (buat[0])
print (buat[1])
print (buat[2])
print ("\n")


buat.append ("Jeruk")
print (buat)

buat.remove ("Apel")
print (buat)

buat.insert (4, "Ramon")
print (buat)

print ("\n")


