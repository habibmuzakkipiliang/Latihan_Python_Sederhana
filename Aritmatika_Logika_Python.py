# Operator Arimatika 

a = 2
b = 5

print ("Tambah = ", a + b)
print ("Kurang = ", a - b)
print ("Kali = ", a * b)
print ("Pangkat = ", a ** b)
print ("Modulus = ", a % b)
print ("Pembagian = ", a / b)
print ("Pembagian bulat = ", a // b, "\n")


# operator Perbandingan dan logika

f = 45
g = 50

print ("Hasilnya : ", f < g)
print ("Hasilnya : ", f > g)
print ("Hasilnya : ", f == g)
print ("Hasinya : ", f >= g)
print ("Hasilnya : ", f <= g, "\n")

print (f > g and f < g)
print (f < g or f > g)
print (not (f < g))
print (not (f > g))